import collections
import logging

import pytest

from solutions.python.aoc import Input, get_puzzles, PuzzleData, Solution


class Year2021Day24(Solution):
    def __init__(self, inp: Input):
        data = inp.get_lines()
        self.cmds = []
        for i in range(14):
            self.cmds.append([d.split() for d in data[i * 18 : (i + 1) * 18]])
        self.g = [(int(c[5][2]), int(c[15][2]), int(c[4][2])) for c in self.cmds]

    def run(self) -> dict:
        def func():
            x = ((z % 26) + k1) != w
            return (z // d * (25 * x + 1)) + (w + k2) * x

        hashes = {0: {0}, 14: {0}}
        # pre-calc possible z values for last 3 steps
        for step in range(13, 10, -1):
            k1, k2, d = self.g[step]
            possible = set()
            for next_z in hashes[step + 1]:
                for z in range(0, 26 ** (14 - step)):
                    for w in range(1, 10):
                        if func() == next_z:
                            possible.add(z)
            logging.debug(f"{step} -> {len(possible)}")
            hashes[step] = possible

        # calc possible z values for every level
        for step in range(0, 14):
            k1, k2, d = self.g[step]
            possible = set()
            limit = hashes.get(step + 1)
            for z in hashes[step]:
                for w in range(1, 10):
                    _z = func()
                    if not limit or _z in limit:
                        possible.add(_z)
            logging.debug(f"{step + 1} => {len(possible)} [{min(possible)}..{max(possible)}]")
            hashes[step + 1] = possible

        # backward scan with paths
        for step in range(13, -1, -1):
            k1, k2, d = self.g[step]
            possible = collections.defaultdict(set)
            for z in hashes[step]:
                for w in range(1, 10):
                    if (_z := func()) in hashes[step + 1]:
                        possible[z].add((w, _z))
            logging.debug(f"{step} ~> {len(possible)} [{min(possible)}..{max(possible)}]")
            hashes[step] = possible

        return hashes

    def part_a_b(self):
        hashes = self.run()
        z_min = z_max = 0
        part_a, part_b = "", ""
        for step in range(14):
            w, z_min = max(hashes[step][z_min])
            part_a += str(w)
            w, z_max = min(hashes[step][z_max])
            part_b += str(w)

        return part_a, part_b


@pytest.mark.parametrize("pd", get_puzzles(), ids=str)
def test_case(pd: PuzzleData):
    pd.check_solution(Year2021Day24)


